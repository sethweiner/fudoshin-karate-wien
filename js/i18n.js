var currentLang = 'de';

function setLanguage(lang) {
  const t = translations[lang];
  if (!t) return;
  currentLang = lang;

  // textContent
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t[el.dataset.i18n];
    if (val) el.textContent = val;
  });

  // innerHTML
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const val = t[el.dataset.i18nHtml];
    if (val) el.innerHTML = val;
  });

  // placeholder
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = t[el.dataset.i18nPlaceholder];
    if (val) el.placeholder = val;
  });

  // aria-label
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const val = t[el.dataset.i18nAria];
    if (val) el.setAttribute('aria-label', val);
  });

  // select options + custom select sync
  document.querySelectorAll('[data-i18n-options]').forEach(select => {
    const key = select.dataset.i18nOptions;
    const opts = t[key];
    if (!opts) return;
    // Update native option text
    Array.from(select.options).forEach((opt, i) => {
      if (opts[i] !== undefined) opt.text = opts[i];
    });
    // Sync custom select wrapper
    const wrapper = select.closest('.custom-select');
    if (wrapper) {
      const buttons = wrapper.querySelectorAll('.custom-select__dropdown button');
      const visibleOpts = opts.slice(1); // custom dropdown skips disabled placeholder
      buttons.forEach((btn, i) => {
        if (visibleOpts[i] !== undefined) btn.textContent = visibleOpts[i];
      });
      const trigger = wrapper.querySelector('.custom-select__trigger');
      if (trigger) {
        if (select.value) {
          trigger.textContent = select.options[select.selectedIndex].text;
        } else {
          trigger.textContent = opts[0];
        }
        trigger.classList.toggle('is-placeholder', !select.value);
      }
    }
  });

  // text after input (radio/checkbox labels)
  document.querySelectorAll('[data-i18n-after]').forEach(el => {
    const val = t[el.dataset.i18nAfter];
    if (!val) return;
    const input = el.querySelector('input');
    if (input) {
      // Find or create text node after input
      let textNode = input.nextSibling;
      if (textNode && textNode.nodeType === 3) {
        textNode.textContent = ' ' + val;
      }
    }
  });

  // Update html lang and title
  document.documentElement.setAttribute('lang', lang === 'bs' ? 'bs' : lang);
  const titles = { de: 'Fudoshin Karate-Do Club Wien', en: 'Fudoshin Karate-Do Club Vienna', tr: 'Fudoshin Karate-Do Kul\u00fcb\u00fc Viyana', bs: 'Fudoshin Karate-Do Klub Be\u010d' };
  document.title = titles[lang] || titles.de;

  // Sync sticker tooltip with tab text
  const stickerEl = document.getElementById('the-sticker');
  if (stickerEl && t['sticker.tab']) stickerEl.dataset.tooltip = t['sticker.tab'];

  // Persist
  localStorage.setItem('fudoshin-lang', lang);
}

// ── LANGUAGE TOGGLE UI ──
(function initLangToggle() {
  const langToggle = document.getElementById("langToggle");
  if (!langToggle) return;
  const langBtn = langToggle.querySelector(".nav__lang-btn");

  langBtn.addEventListener("click", (e) => {
e.stopPropagation();
const isOpen = langToggle.classList.contains("is-open");
langToggle.classList.toggle("is-open", !isOpen);
langBtn.setAttribute("aria-expanded", String(!isOpen));
  });

  langToggle.querySelectorAll("[data-lang]").forEach(btn => {
btn.addEventListener("click", () => {
  langBtn.childNodes[0].textContent = btn.dataset.lang === "bs" ? "BKS" : btn.dataset.lang.toUpperCase();
  langToggle.classList.remove("is-open");
  langBtn.setAttribute("aria-expanded", "false");
  setLanguage(btn.dataset.lang);
});
  });

  document.addEventListener("click", (e) => {
if (!langToggle.contains(e.target)) {
  langToggle.classList.remove("is-open");
  langBtn.setAttribute("aria-expanded", "false");
}
  });
  document.addEventListener("keydown", e => {
if (e.key === "Escape") {
  langToggle.classList.remove("is-open");
  langBtn.setAttribute("aria-expanded", "false");
}
  });
})();

